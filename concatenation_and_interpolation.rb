# Concatenation & Interpolation ---------------------> https://www.rubyguides.com/2019/07/ruby-string-concatenation/

# Concatenation looks like this:

a = "Nice to meet you"
b = ", "
c = "do you like blueberries?"

puts(a + b + c)

# Interpolation or merging of variables into strings is a powerful technique.

age  = 33
name = "John"
puts "Hello, my name is #{name} & I'm #{age} years old."
